package com.app.api.DTO;

public class DTOBasicoPerfilPiloto{
    public String imgPerfil;
    public String imgCoche;
    public String nombreCompleto;
    public String fechaNac;
    public int altura;
    public int peso;
    public int numero;
    public String nombreEscuderia;
    
    public String getImgPerfil() {
        return imgPerfil;
    }
    public void setImgPerfil(String imgPerfil) {
        this.imgPerfil = imgPerfil;
    }
    public String getImgCoche() {
        return imgCoche;
    }
    public void setImgCoche(String imgCoche) {
        this.imgCoche = imgCoche;
    }
    public String getNombreCompleto() {
        return nombreCompleto;
    }
    public void setNombreCompleto(String nombreCompleto) {
        this.nombreCompleto = nombreCompleto;
    }
    public String getFechaNac() {
        return fechaNac;
    }
    public void setFechaNac(String fechaNac) {
        this.fechaNac = fechaNac;
    }
    public int getAltura() {
        return altura;
    }
    public void setAltura(int altura) {
        this.altura = altura;
    }
    public int getPeso() {
        return peso;
    }
    public void setPeso(int peso) {
        this.peso = peso;
    }
    public int getNumero() {
        return numero;
    }
    public void setNumero(int numero) {
        this.numero = numero;
    }
    public String getNombreEscuderia() {
        return nombreEscuderia;
    }
    public void setNombreEscuderia(String nombreEscuderia) {
        this.nombreEscuderia = nombreEscuderia;
    }

    
}
