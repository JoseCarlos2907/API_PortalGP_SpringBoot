package com.app.api.DTO;

public class DTOPerfilEscuderia{
    public String nombre;
    public String imgLogo;
    public String imgEscuderia;
    public String descripcion;
    public String imgPrincipal;
    public String segundaImg;
    public String terceraImg;
    public String cuartaImg;
    
    public String getNombre() {
        return nombre;
    }
    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    public String getImgLogo() {
        return imgLogo;
    }
    public void setImgLogo(String imgLogo) {
        this.imgLogo = imgLogo;
    }
    public String getImgEscuderia() {
        return imgEscuderia;
    }
    public void setImgEscuderia(String imgEscuderia) {
        this.imgEscuderia = imgEscuderia;
    }
    public String getDescripcion() {
        return descripcion;
    }
    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }
    public String getImgPrincipal() {
        return imgPrincipal;
    }
    public void setImgPrincipal(String imgPrincipal) {
        this.imgPrincipal = imgPrincipal;
    }
    public String getSegundaImg() {
        return segundaImg;
    }
    public void setSegundaImg(String segundaImg) {
        this.segundaImg = segundaImg;
    }
    public String getTerceraImg() {
        return terceraImg;
    }
    public void setTerceraImg(String terceraImg) {
        this.terceraImg = terceraImg;
    }
    public String getCuartaImg() {
        return cuartaImg;
    }
    public void setCuartaImg(String cuartaImg) {
        this.cuartaImg = cuartaImg;
    }

    
}
